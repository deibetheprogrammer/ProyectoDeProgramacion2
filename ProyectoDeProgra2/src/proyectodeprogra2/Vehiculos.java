    private void B_Register_RegisterNaAMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_B_Register_RegisterNaAMouseClicked
        // TODO add your handling code here:
        
        String nickname = TF_Nickname_RegisterNaA.getText();
        String password = PF_password_RegisterNaA.getText();
        String pais = L_PaisDeNacimiento_RegisterNaA.getSelectedValue();
        Date birthday = DC_Birthday_RegisterNaA.getDate();
        String correo = TF_Correo_RegisterNaA.getText();
        String nombre = TF_NombreCompleto_RegisterNaA.getText();
        double latitud = Double.parseDouble(TF_Latitud_RegisterNaA.getText());
        double longitud = Double.parseDouble(TF_Longitud_RegisterNaA.getText());
        
        boolean correct;
        
        String mail = "\\S+(@{1})\\S+(.{1})\\S+";
        
        Pattern p = Pattern.compile(mail);
        Matcher m = p.matcher(correo);
        
        correct = m.matches();
        
        if(correct){
        
            //Se verifica que el usuario no exista
            boolean pass = true;

            for (Ofertador ofertador : ofertadores) {
                if (ofertador.getNickname().equals(nickname)) {
                    pass = false;
                    JOptionPane.showMessageDialog(this, "Su nickname no está disponible");
                    break;
                } else if (ofertador.getCorreo().equals(correo)) {
                    pass = false;
                    JOptionPane.showMessageDialog(this, "Su correo ya está ocupado");
                    break;
                }
            }

            for (Cliente cliente : clientes) {
                if (cliente.getNickname().equals(nickname)) {
                    pass = false;
                    JOptionPane.showMessageDialog(this, "Su nickname no está disponible");
                    break;
                } else if (cliente.getCorreo().equals(correo)) {
                    pass = false;
                    JOptionPane.showMessageDialog(this, "Su correo ya está ocupado");
                    break;
                }
            }
            if (pass) {

                if (CB_TipoUsuario_RegisterNaA.getSelectedItem().equals("Ofertador")) {

                    ofertadores.add(new Ofertador(nickname, password, pais, birthday,latitud,longitud,false,new Date(), correo, nombre));
                    guardarOfertadores(ofertadores);
                    
                    JOptionPane.showMessageDialog(D_RegisterNaA, "Registrado correctamente");
                    
                    D_RegisterNaA.setVisible(false);

                    D_LogIn.pack();
                    D_LogIn.setModalityType(Dialog.ModalityType.APPLICATION_MODAL);
                    D_LogIn.setVisible(true);

                } else {

                    clientes.add(new Cliente(nickname, password, pais, birthday,latitud,longitud,false,new Date(), correo, nombre));
                    guardarClientes(clientes);
                    
                    JOptionPane.showMessageDialog(D_RegisterNaA, "Registrado correctamente");
                    
                    D_RegisterNaA.setVisible(false);

                    D_LogIn.pack();
                    D_LogIn.setModalityType(Dialog.ModalityType.APPLICATION_MODAL);
                    D_LogIn.setVisible(true);
                }
            }

        }
        else {
            JOptionPane.showMessageDialog(this, "Correo invalido");
        }
    }//GEN-LAST:event_B_Register_RegisterNaAMouseClicked

