    public static String consultarBanco(String tarjeta, String nombre, String vencimiento, String codigoSeguridad, int precio) {
        // A Java program for a Client 

        // initialize socket and input output streams 
        Socket socket = null;
        DataInputStream input = null;
        DataOutputStream out = null;
        
        String address = "localhost";
        int port = 10000;
        
        String sp = ";";
        String mensaje = tarjeta + sp + nombre + sp + vencimiento + sp + codigoSeguridad + sp + precio;

        // establish a connection 
        try {
            socket = new Socket(address, port);
            System.out.println("Connected");

            // takes input from terminal 
            input = new DataInputStream(socket.getInputStream());
            
            // sends output to the socket 
            out = new DataOutputStream(socket.getOutputStream());
        } catch (UnknownHostException u) {
            System.out.println(u);
            JOptionPane.showMessageDialog(null, "No se pudo conectar con el banco");
            return "-1";
        } catch (IOException i) {
            System.out.println(i);
            JOptionPane.showMessageDialog(null, "No se pudo comunicar con el banco");
            return "-1";
        }

        // string to read message from input 
        //String line = "";
        
        String outcome = "-1";
        
        try {
            out.writeUTF(mensaje);
            String respuestaBanco = input.readUTF();
            outcome = respuestaBanco;
            
        } catch (IOException i) {
            System.out.println(i);
        }


        // close the connection 
        try {
            input.close();
            out.close();
            socket.close();
        } catch (IOException i) {
            System.out.println(i);
        }
        
        return outcome;
    }
    
   public static void sendMail(String correo, String nickname, String password) {
       String miUsuario = "";
       String mipassword = "";
       try (BufferedReader br = new BufferedReader(new FileReader("../../myMail.txt"))) {
           String line;
           String[] tokens;
           line = br.readLine();
           tokens = line.split(";");
           miUsuario = tokens[0];
           mipassword = tokens[1];
       } catch (FileNotFoundException ex) {
            ex.printStackTrace();
        } catch (IOException ex) {
            ex.printStackTrace();
        }
       
         Properties propiedad = new Properties();
        propiedad.setProperty("mail.smtp.host", "smtp.gmail.com");
        propiedad.setProperty("mail.smtp.starttls.enable", "true");
        propiedad.setProperty("mail.smtp.port", "587");
        propiedad.setProperty("mail.smtp.auth","true");
        

        
        Session sesion = Session.getDefaultInstance(propiedad);
        String correoEnvia = miUsuario;
        String contrasena = mipassword;
        String receptor = correo;
        String asunto = "Recuperacion de credenciales Vehiculos.com";
        String mensaje = "De parte del equipo de Vehiculos.com le saludamos \n"
                + "Usted solicitó recuperar sus credenciales, se las proporcionamos a continuación: \n"
                + "Nickname: " + nickname + "\n"
                + "Contraseña: " + password + "\n"
                + "Vehiculos.com le desea un buen día.";
        
        MimeMessage mail = new MimeMessage(sesion);
        try {
            mail.setFrom(new InternetAddress (correoEnvia));
            mail.addRecipient(Message.RecipientType.TO, new InternetAddress (receptor));
            mail.setSubject(asunto);
            mail.setText(mensaje);

            Transport transportar = sesion.getTransport("smtp");
            transportar.connect(correoEnvia, contrasena);
            transportar.sendMessage(mail, mail.getRecipients(Message.RecipientType.TO));
            transportar.close();
            
            JOptionPane.showMessageDialog(null, "Listo, revise su correo");

        } catch (AddressException ex) {
            System.out.println(ex);
        } catch (MessagingException ex) {
            System.out.println(ex);
        }
    }

